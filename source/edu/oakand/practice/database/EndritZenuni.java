package edu.oakland.helper.database;


//practice for github
public class EndritZenuni {

  private int numX;
  private int numY;

  public EndritZenuni(int numX, int numY) {
    this.numX = numX;
    this.numY = numY;
  }

  public int add() {
    return numX + numY;
  }

  public int subtract() {
    return numX - numY;
  }

}
